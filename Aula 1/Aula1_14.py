
# For Loops (Looping)

for letra in 'Google':
    print(letra)

print('')

palavra = 'Google'
for letra in palavra:
    print(letra)

print('')

palavra = 'Google'
for letra in palavra:
    print(letra + ' está dentro da palavra Google')

print('')

palavra = 'Google'
for letra in palavra:
    print(f'{letra} está dentro da palavra Google')

print('')

palavra = 'Espetacular'
for letra in palavra:
    print(f'{letra} está dentro da palavra {palavra}')